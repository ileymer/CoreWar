#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*start;

	if (n + 1 < n)
		return (NULL);
	start = s1;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0' && n--)
	{
		*s1 = *s2;
		s2++;
		s1++;
	}
	*s1 = '\0';
	return (start);
}
